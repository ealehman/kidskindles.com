        <h2 class="featurette-heading">My Experience<span class="muted"></h2> <br>
        <p class="lead">In December 2011 I visited Nicaragua with my family and was introduced to the work of the Foundation for Sustainable Development in Limon.  My brother and I joined the kids on the dusty town fields, shooing away the scurrying chickens and sleeping pigs, so we could show them the sporting equipment we had collected and brought with us.  I developed an instant camaraderie with the kids who shared my love of softball.
Soon after I returned home, I got in touch with Michelle Chackerian, who was spearheading the local literacy program and had created the community library.  The problem of childhood illiteracy in Limon had a startlingly identifiable cause:  There were no books.  The older children, for whom illiteracy was about to become a lifelong stain, had no materials to engage them at all.  I immediately thought about how effective e-readers could be for these kids; for certain I knew how much an e-reader could create curiosity and sustain interest in kids who were exactly my age.  When I suggested the idea, it was met with predictable skepticism, but I was so convinced that I volunteered to take on the project, raise the money at home, and ship the e-readers to Limon.  And that is exactly what I did. <br></br>
From the moment the readers arrived in the community, they became a local treasure.  Teenage children gathered to form book clubs; they wanted to read and they wanted to share what they were learning. The library volunteers pelted me with emails describing the new programs they were building around the e-readers and the benefit that had accrued to even the adults who came to the library to see the new "books."
This past July, I returned to Limon, alone, without my family, in the hopes of seeing for myself exactly what I had begun to create.  I watched the piles of bicycles stack up outside the library every single day as the news of the arrival of even more e-readers spread through the community like yellow fever.  I spent every day at the library immersing myself in the book club and both learning from and teaching the kids who gathered there.  When I came home from Limon, I realized that I now truly understood the value of knowing a community from within.
</p>
